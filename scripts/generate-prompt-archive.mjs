#!/usr/bin/env node
/**
 * Prompt archive generator — Next Chapter Project deliverable.
 *
 * Regenerates docs/05-development/prompt-archive/ from two sources:
 *   1. The curated prompt history (docs/05-development/prompt-history.md),
 *      split on section boundaries into history-NN.md chunks.
 *   2. Full commit history (git log, oldest first) including AI session
 *      trailers, split into commits-NN.md chunks. Oldest-first ordering keeps
 *      earlier chunks stable, so each new commit only touches the last file.
 *
 * Every emitted file is hard-capped at MAX_CHARS (GitHub markdown rendering
 * limit per instructor requirements); the script fails the run if a chunk
 * cannot be kept under the cap.
 *
 * Run locally:  node scripts/generate-prompt-archive.mjs
 * Run in CI:    .github/workflows/prompt-archive.yml (every push to main)
 */

import { execFileSync } from 'node:child_process';
import { mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const MAX_CHARS = 8000; // enforced in UTF-8 BYTES (>= char count)
const size = (s) => Buffer.byteLength(s, 'utf8');
const ROOT = new URL('..', import.meta.url).pathname;
const OUT_DIR = join(ROOT, 'docs/05-development/prompt-archive');
const HISTORY_FILE = join(ROOT, 'docs/05-development/prompt-history.md');

const FIELD = '\x1f';
const RECORD = '\x1e';

function chunkHeader(title, part, note) {
  return (
    `# ${title} (part ${part})\n\n` +
    `> ${note}\n` +
    '> Auto-generated by `scripts/generate-prompt-archive.mjs` - do not edit by hand.\n\n'
  );
}

/** Greedily pack entries into chunks; every emitted file stays under MAX_CHARS. */
function packChunks(entries, title, note) {
  const headerLen = size(chunkHeader(title, 99, note));
  const chunks = [];
  let current = '';
  for (const entry of entries) {
    if (size(entry) + headerLen > MAX_CHARS) {
      throw new Error(
        `Single entry exceeds ${MAX_CHARS} bytes (${size(entry)}); split it upstream:\n` +
          entry.slice(0, 120),
      );
    }
    if (current && size(current) + size(entry) + headerLen > MAX_CHARS) {
      chunks.push(current);
      current = '';
    }
    current += entry;
  }
  if (current) chunks.push(current);
  return chunks.map((body, i) => {
    const text = chunkHeader(title, i + 1, note) + body;
    if (size(text) > MAX_CHARS) {
      throw new Error(`Chunk ${i + 1} of "${title}" is ${size(text)} bytes (max ${MAX_CHARS})`);
    }
    return text;
  });
}

/* ---- Source 1: curated prompt history, split on ## / ### headings -------- */
const history = readFileSync(HISTORY_FILE, 'utf8');
const historyEntries = history
  .split(/\n(?=#{2,3} )/)
  .map((s) => (s.endsWith('\n') ? s : s + '\n'))
  .filter((s) => s.trim());

/* ---- Source 2: every commit, oldest first ------------------------------- */
const log = execFileSync(
  'git',
  ['log', '--reverse', `--format=%H${FIELD}%as${FIELD}%an${FIELD}%s${FIELD}%b${RECORD}`],
  { cwd: ROOT, encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 },
);
const commitEntries = log
  .split(RECORD)
  .map((s) => s.trim())
  .filter(Boolean)
  .map((record) => {
    const [hash, date, author, subject, body = ''] = record.split(FIELD);
    let entry = `## ${date} - ${subject}\n\n- Commit: \`${hash.slice(0, 10)}\`\n- Author: ${author}\n`;
    const lines = body.split('\n');
    const trailers = lines.filter((l) => /^(Claude-Session|Co-Authored-By):/i.test(l.trim()));
    for (const t of trailers) entry += `- ${t.trim()}\n`;
    const prose = lines
      .filter((l) => !/^(Claude-Session|Co-Authored-By):/i.test(l.trim()))
      .join('\n')
      .trim();
    if (prose) {
      // Bound commit bodies so a single entry can never blow the file cap.
      const clipped =
        prose.length > 1500 ? prose.slice(0, 1500) + '\n...(truncated; see git log)' : prose;
      entry += '\n' + clipped + '\n';
    }
    return entry + '\n---\n\n';
  });

/* ---- Emit ---------------------------------------------------------------- */
rmSync(OUT_DIR, { recursive: true, force: true });
mkdirSync(OUT_DIR, { recursive: true });

const manifest = [];

packChunks(
  historyEntries,
  'Prompt History - Curated',
  'Human-curated prompts and outcomes, sourced from docs/05-development/prompt-history.md.',
).forEach((text, i) => {
  const name = `history-${String(i + 1).padStart(2, '0')}.md`;
  writeFileSync(join(OUT_DIR, name), text);
  manifest.push({ name, chars: size(text) });
});

packChunks(
  commitEntries,
  'Commit Prompt Trail',
  'Every commit with its message, authorship, and AI session trailers - the reproducible record of project inputs.',
).forEach((text, i) => {
  const name = `commits-${String(i + 1).padStart(2, '0')}.md`;
  writeFileSync(join(OUT_DIR, name), text);
  manifest.push({ name, chars: size(text) });
});

const head = execFileSync('git', ['rev-parse', '--short', 'HEAD'], {
  cwd: ROOT,
  encoding: 'utf8',
}).trim();

const index =
  '# Prompt Archive\n\n' +
  '> Auto-generated by `scripts/generate-prompt-archive.mjs` - do not edit by hand.\n' +
  `> Regenerated as of commit \`${head}\`. Every file is capped at ${MAX_CHARS} characters\n` +
  '> per Next Chapter Project instructor requirements.\n\n' +
  '| File | Characters |\n|---|---|\n' +
  manifest.map((f) => `| [${f.name}](${f.name}) | ${f.chars} |`).join('\n') +
  '\n\nSources: `docs/05-development/prompt-history.md` (curated) and `git log` (commit trail).\n';
if (size(index) > MAX_CHARS) throw new Error('Index file exceeds cap');
writeFileSync(join(OUT_DIR, 'README.md'), index);

console.log(`prompt-archive: wrote ${manifest.length + 1} files (max ${MAX_CHARS} chars each)`);
for (const f of manifest) console.log(`  ${f.name}  ${f.chars} chars`);
